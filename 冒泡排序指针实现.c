//using namespace std;
#define N 10
int a[12];
int *p[12]; 
void swap(int **x,int **y){
	int *t=*x;
	*x=*y;
	*y=t;
}
int main(){
	while(scanf("%d",&a[1])!=EOF){
		for(int i=2;i<=N;i++)
			scanf("%d",&a[i]);
		for(int i=1;i<=N;i++)
			p[i]=&a[i];
		for(int i=1;i<=N;i++)
			for(int j=i+1;j<=N;j++)
				if(*p[i]>*p[j])
					swap(&p[i],&p[j]);	
		for(int i=1;i<=9;i++)
			printf("%d->",*p[i]);
		printf("%d\n",*p[10]);				
	}
	return 0;
}
